#include "libft.h"

char	*ft_strdup(char const *s1)
{
	char	*str;
	size_t	i;

	i = 0;
	if (!(str = ft_strnew(ft_strlen(s1))))
		return (NULL);
	while (s1[i])
	{
		str[i] = s1[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
